import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dataDir = path.join(__dirname, "..", "data", "cuv");
const bundleFile = path.join(__dirname, "..", "src", "bible-data-bundle.ts");
const jsonFile = path.join(__dirname, "..", "bible-data.json");

const files = fs.readdirSync(dataDir).filter(f => f.endsWith(".json") && f !== "books.json");

// Generate TypeScript bundle (for embedded build)
const records = [];
for (const file of files) {
  const raw = fs.readFileSync(path.join(dataDir, file), "utf-8");
  const data = JSON.parse(raw);
  if (!data.bookId) continue;
  records.push(`  "${data.bookId}": ${JSON.stringify(data)}`);
}

const tsOutput = `// Auto-generated by scripts/bundle-data.mjs — do not edit.
import type { BookData } from "./types";

export const BIBLE_DATA: Record<string, BookData> = {
${records.join(",\n")}
};
`;

fs.writeFileSync(bundleFile, tsOutput);
console.log(`Bundled ${records.length} books into ${bundleFile}`);

// Generate single JSON file (for lazy download)
const jsonData = {};
for (const file of files) {
  const raw = fs.readFileSync(path.join(dataDir, file), "utf-8");
  const data = JSON.parse(raw);
  if (!data.bookId) continue;
  jsonData[data.bookId] = data;
}

fs.writeFileSync(jsonFile, JSON.stringify(jsonData));
console.log(`Generated ${jsonFile} (${(fs.statSync(jsonFile).size / 1024 / 1024).toFixed(1)}MB)`);
