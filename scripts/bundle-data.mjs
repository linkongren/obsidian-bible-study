/**
 * Read all JSON files from data/cuv/ and generate a TypeScript bundle.
 * Run before building to embed Bible data into main.js.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const dataDir = path.join(__dirname, "..", "data", "cuv");
const outFile = path.join(__dirname, "..", "src", "bible-data-bundle.ts");

const files = fs.readdirSync(dataDir).filter(f => f.endsWith(".json"));

const records = [];
for (const file of files) {
  if (file === "books.json") continue;
  const raw = fs.readFileSync(path.join(dataDir, file), "utf-8");
  const data = JSON.parse(raw);
  if (!data.bookId) continue;
  records.push(`  "${data.bookId}": ${JSON.stringify(data)}`);
}

const output = `// Auto-generated by scripts/bundle-data.mjs — do not edit.
import type { BookData } from "./types";

export const BIBLE_DATA: Record<string, BookData> = {
${records.join(",\n")}
};
`;

fs.writeFileSync(outFile, output);
console.log(`Bundled ${records.length} books into ${outFile}`);
